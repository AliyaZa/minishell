#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*src;
	int				i;

	i = 0;
	src = (unsigned char *)s;
	while (n > 0)
	{
		src[i] = '\0';
		i++;
		n--;
	}
}
