#include "../minishell.h"

void	pwd(void)
{
	char	*pwd;

	pwd = NULL;
	pwd = getcwd(pwd, PATH_MAX);
	write(1, pwd, ft_strlen(pwd));
	write(1, "\n", 1);
}
