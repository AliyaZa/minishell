#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*ptr;
	int		i;

	ptr = NULL;
	i = 0;
	if (!(ptr = (char *)malloc(sizeof(*ptr) * (ft_strlen(str) + 1))))
		return (NULL);
	while (str[i] != '\0')
	{
		ptr[i] = str[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
